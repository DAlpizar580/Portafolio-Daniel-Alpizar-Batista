    private void Panel_botonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Panel_botonMouseClicked
        // Se establece el color blanco para los mensajes de error y de información
error_contraseña.setForeground(Color.white);
error_cedula.setForeground(Color.white);
error_edad.setForeground(Color.white);
error_pais.setForeground(Color.white);
info_editada.setForeground(Color.white);

// Se crea una instancia de la clase 'paciente'
paciente editar = new paciente();

// Se declara y se inicializa una lista 'salida' para almacenar los resultados de la edición de datos
ArrayList<String> salida = new ArrayList<>();
salida = editar.editar_datos(this.nombre, txt_field_contraseña.getText(), Label_correo.getText(), txt_field_cedula.getText(), txt_field_edad.getText(), txt_field_pais.getText(), txt_field_padecimiento.getText());

// Se verifica el resultado de la edición de datos
if (salida.get(0).equals("correcto")) {
    // Si la edición fue correcta, se cambia el color del mensaje 'info_editada' a verde
    info_editada.setForeground(new Color(0,153,0)); 
}

// Se actualiza el color de los mensajes de error basado en los resultados de la edición
if (salida.get(1).equals("t")) {
    error_contraseña.setForeground( Color.red); 
}
if (salida.get(2).equals("t")) {
    error_cedula.setForeground( Color.red); 
}
if (salida.get(3).equals("t")) {
    error_edad.setForeground( Color.red); 
}
if (salida.get(4).equals("t")) {
    error_pais.setForeground( Color.red); 
}

    }//GEN-LAST:event_Panel_botonMouseClicked

    
    // Método que restringe la entrada de caracteres en un campo de texto para que solo sean dígitos
    private void txt_field_cedulaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_field_cedulaKeyTyped
        char c = evt.getKeyChar();
        if(c<'0' || c>'9') evt.consume(); // Si el caracter ingresado no es un dígito, consume el evento para ignorar la entrada
    }//GEN-LAST:event_txt_field_cedulaKeyTyped

    
