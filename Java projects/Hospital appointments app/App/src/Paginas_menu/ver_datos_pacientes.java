    // Método que restringe la entrada de caracteres en un campo de texto para que sean letras (minúsculas o mayúsculas)
    private void txt_field_padecimientoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_field_padecimientoKeyTyped
        char c = evt.getKeyChar();
        // Si el caracter ingresado no es una letra (ni minúscula ni mayúscula), consume el evento para ignorar la entrada
        if((c<'a' || c>'z') && (c<'A') | c>'Z') evt.consume();
    }//GEN-LAST:event_txt_field_padecimientoKeyTyped

