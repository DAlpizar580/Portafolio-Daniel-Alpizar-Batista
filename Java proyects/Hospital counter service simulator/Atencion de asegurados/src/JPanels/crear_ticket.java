    private void Label_boton_ingresarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Label_boton_ingresarMousePressed
        try
        {
            if(Info_Cuentas.getFila().Buscar(String.valueOf(txt_field_cedula.getText())) != null)
            {
                menu_asegurados menu_asegurados = new menu_asegurados(String.valueOf(txt_field_cedula.getText()), Info_Cuentas.getFila().Buscar(String.valueOf(txt_field_cedula.getText())));
                menu_asegurados.initialize();
                ventana.dispose();
            }
            else if (cajero1.getAsegurado().getCedula().equals(String.valueOf(txt_field_cedula.getText())))
            {
                menu_asegurados menu_asegurados = new menu_asegurados(String.valueOf(txt_field_cedula.getText()), cajero1.getAsegurado());
                menu_asegurados.initialize();
                ventana.dispose();
            }
            else if (cajero2.getAsegurado().getCedula().equals(String.valueOf(txt_field_cedula.getText())))
            {
                menu_asegurados menu_asegurados = new menu_asegurados(String.valueOf(txt_field_cedula.getText()), cajero2.getAsegurado());
                menu_asegurados.initialize();
                ventana.dispose();
            }
            else if (cajero3.getAsegurado().getCedula().equals(String.valueOf(txt_field_cedula.getText())))
            {
                menu_asegurados menu_asegurados = new menu_asegurados(String.valueOf(txt_field_cedula.getText()), cajero3.getAsegurado());
                menu_asegurados.initialize();
                ventana.dispose();
            }
            else
            {
                Label_datos_incorrectos.setText("La cedula no existe o ya fue antendido");
                Label_datos_incorrectos.setForeground(Color.red);
            }
        }
        catch(Exception e)
        {
            Label_datos_incorrectos.setText("La cedula no existe o ya fue antendido");
            Label_datos_incorrectos.setForeground(Color.red);
        }
    }//GEN-LAST:event_Label_boton_ingresarMousePressed

    private void Boton_crear_ticketMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Boton_crear_ticketMouseClicked
        if (String.valueOf(txt_field_cedula.getText()).length() == 9)
        {
            Asegurado asegurado = new Asegurado(String.valueOf(txt_field_cedula.getText()), "", "", "", "", "");
            Info_Cuentas.getFila().cola_asegurados(asegurado);
            menu_asegurados menu_asegurados = new menu_asegurados(String.valueOf(txt_field_cedula.getText()), asegurado);
            menu_asegurados.initialize();
            ventana.dispose();
        }
        else
        {
            Label_datos_incorrectos.setText("La cedula debe tener 9 digitos");
            Label_datos_incorrectos.setForeground(Color.red);
        }
    }//GEN-LAST:event_Boton_crear_ticketMouseClicked

    private void txt_field_cedulaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_field_cedulaKeyTyped
        char c = evt.getKeyChar();
        // Consume el evento si el caracter ingresado no es un d√≠gito del 0 al 9
        if(c<'0' || c>'9') evt.consume();
    }//GEN-LAST:event_txt_field_cedulaKeyTyped


